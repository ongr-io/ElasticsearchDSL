declare(strict_types=1);

namespace ONGR\ElasticsearchDSL;

trait NameAwareTrait
{
    private ?string $name = null;

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(?string $name): static
    {
        $this->name = $name;

        return $this;
    }
}
