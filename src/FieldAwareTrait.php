namespace ONGR\ElasticsearchDSL;

trait FieldAwareTrait
{
    /**
     * @var string
     */
    private $field;

    /**
     * @return mixed
     */
    public function getField()
    {
        return $this->field;
    }

    /**
     * @param string $field
     *
     * @return $this
     */
    public function setField($field)
    {
        $this->field = $field;

        return $this;
    }
}
